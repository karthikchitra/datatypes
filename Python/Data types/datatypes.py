ab=5 # Integer
print(ab)
print(type(ab))
print(int())# default value int is 0

ac=5.0 # float
print(ac)
print(type(ac))
print(float()) # default value  float is 0.0

c=True # False
print(c)
print(type(c))
print(bool()) # default value is always False

ad =4+3j # complex
print(ad)
print(type(ad))
print(complex()) # default value is complex is 'oj'
